package com.java.demo.comment;

/**
 * 3.文档注释
 * 这是一个程序
 * 是用来讲解注释的使用
 */
public class CommentDemo {
    /**
     * 这是一个 main 方法，是程序的入口
     * 必须要有这个 main 方法，程序才能执行
     */
    public static void main(String[] args) {
        // 目标：认识三种注释的写法
        // 1.单行注释
        // 打印2行诗句
        System.out.println("我欲乘风归去，又恐琼楼玉宇");

        /*
            2.多行注释
            打印3行诗句
        */
        System.out.println("高处不胜寒");
        System.out.println("起舞弄清影");
        System.out.println("何似在人间");
    }
}
